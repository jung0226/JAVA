class WhileTest1 
{
	public static void main(String[] args) 
	{
		int i=0;//수정불가
		while(i<5){
			i++;//++i, i=i+1, i+=1
			System.out.println(i);
			
		}
		System.out.println("The End");
	}//end of main
}
